/*Task 1 — Employee Salary & Bonus Calculation System

Objective

To read employee details from application.properties using @Value and calculate the yearly 
salary including bonus.

Description

In many organizations, employee salary details are stored in configuration files.
This task helps you understand how to inject those values into a Spring Boot application 
and perform calculations using them.

The system should:

Read employee ID, name, monthly salary, and bonus

Calculate yearly salary using logic:

yearly salary = (monthly salary × 12) + bonus

Display all details in a formatted way

Input (application.properties)

Employee Name

Employee ID

Monthly Salary

Bonus Amount

Processing Logic

Inject values using @Value

Perform arithmetic calculation for yearly salary

Store result in a variable

Expected Output
----------------
Employee details

Monthly salary

Bonus

Final yearly salary*/
